namespace CalculatorApp.Calculators
{
    internal static class HyperbolicCalculator
    {
        public static double Sinh(double x)
        {
            return Math.Sinh(x);
        }

        public static double Cosh(double x)
        {
            return Math.Cosh(x);
        }

        public static double Tanh(double x)
        {
            return Math.Tanh(x);
        }
    }
}
